    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        arreglo.add(new Estudiante(txtId.getText(), txtNombre.getText(), txtApellido.getText()));
        try {
            FileOutputStream archivo= new FileOutputStream("elarchivo.dat");
            ObjectOutputStream objArchivo= new ObjectOutputStream(archivo);
            objArchivo.writeObject(arreglo);
            objArchivo.close();
            lblMensaje.setText("archivo guardado");
            
            
        } catch (Exception ex) {
            lblMensaje.setText("Error");
            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerActionPerformed
        try{
            FileInputStream archivo= new FileInputStream("elarchivo.dat");
            ObjectInputStream objArchivo=new ObjectInputStream(archivo);
            arreglo=(ArrayList<Estudiante>) objArchivo.readObject();
            lblMensaje.setText("Archivo leido");
            listar();
        }   catch(Exception ex){
            lblMensaje.setText("Error");
        }
        
    }//GEN-LAST:event_btnLeerActionPerformed

