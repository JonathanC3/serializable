    public void listar(){
        modelo= new DefaultListModel();
        //ciclo que recorre el arreglo para cargar el modelo
        for (int i = 0; i < arreglo.size(); i++) {
            modelo.addElement(arreglo.get(i).getId() +" "+arreglo.get(i).getNombre() +" "+arreglo.get(i).getApellido());
        }
        //agregar el modelo al jlist
        lstInformacion.setModel(modelo);
    }
    
